const dates = extractions.dates.map(obj => obj.date);

// DateRange
const dateRange = [dates[0], dates[dates.length - 1]];

// Honestly, just googled how to generate random color string. lol
const getRandomColor = () => {
  return "#" + Math.random().toString(16).substr(-6);
}

// Abstract function to extract and format values so as to be more DRY.
const extractData = (array, siteIndex, wellIndex, siteName, resource) => {
  let initialValue = parseInt(initialData.sites[siteIndex].wells[wellIndex][siteName][resource], 10);
  let values = [initialValue];
  let dataPoints = array.map(obj => {
    initialValue -= obj.data.sites[siteIndex].wells[wellIndex][siteName][resource];
    return initialValue
  })
  return values.concat(dataPoints);
}

// Returns properly formated traces for each graph
const formatTraces = (wellNames, siteIndex, resource, dates) => {
  return wellNames.map((name, index) => ({
    type: "scatter",
    mode: "lines",
    name,
    x: dates,
    y: extractData(extractions.dates, siteIndex, index, name, resource),
    line: { color: getRandomColor() }
  })
  )
}

// Return layout object for graph
const generateLayout = (title, xrange, yrange) => ({
  title,
  xaxis: {
    range: xrange,
    type: 'date'
  },
  yaxis: {
    range: yrange,
    type: 'linear'
  }
})


